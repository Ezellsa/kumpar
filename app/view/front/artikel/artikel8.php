    <h1>5 tokoh binaraga yang terkenal di dunia</h1>
    <br>
    <h2>1.Arnold Schwarzenegger</h2>
    <img src="<?=base_url()?>media/arnold.jpeg">
    <p> Dilahirkan di Austria pada tahun 1947, Arnold Schwarzenegger merupakan salah satu tokoh paling ikonik dalam sejarah bodybuilding. Ia memenangkan gelar Mr. Olympia tujuh kali dan dikenal karena fisiknya yang luar biasa, disiplin latihannya, dan kepribadiannya yang karismatik. Selain itu, Schwarzenegger juga merupakan aktor terkenal dan politisi, menjabat sebagai Gubernur California dari 2003 hingga 2011.</p>
<br>
<h2>2.Ronnie Coleman</h2>
<img src="<?=base_url()?>media/ronnie.jpeg">
<p>Dikenal dengan julukan "The King", Ronnie Coleman adalah salah satu bodybuilder paling sukses sepanjang masa. Ia memenangkan gelar Mr. Olympia sebanyak delapan kali, menyamai rekor Arnold Schwarzenegger. Coleman terkenal dengan fisiknya yang luar biasa besar dan berotot, serta dedikasinya yang luar biasa terhadap latihan beban yang intens.</p>
<br>
<h2>3.Jay Cutler</h2>
<img src="<?=base_url()?>media/jay.jpeg">
<p> Jay Cutler adalah seorang bodybuilder profesional Amerika yang dikenal karena penampilannya yang konsisten dan pesaing yang tangguh. Ia memenangkan gelar Mr. Olympia sebanyak empat kali dan menjadi salah satu figur terkemuka dalam industri kebugaran. Cutler terkenal dengan pendekatannya yang profesional terhadap olahraga dan komitmennya terhadap pengembangan tubuh.</p>
<br>
<h2>4.Dorian Yates</h2>
<img src="<?=base_url()?>media/dorian.jpeg">
<p>Dikenal dengan julukan "The Shadow", Dorian Yates adalah seorang bodybuilder Inggris yang meraih gelar Mr. Olympia enam kali antara tahun 1992 dan 1997. Yates terkenal karena fisiknya yang sangat bermuscle mass dan pendekatannya yang revolusioner terhadap latihan beban, termasuk teknik High-Intensity Training (HIT).</p>
<br>
<h2>5.Sergio Oliva</h2>
<img src="<?=base_url()?>media/oliva.jpeg">
<p>Dikenal sebagai "The Myth", Sergio Oliva adalah salah satu bodybuilder paling dominan pada tahun 1960-an dan 1970-an. Ia memenangkan gelar Mr. Olympia tiga kali berturut-turut dari tahun 1967 hingga 1969. Oliva terkenal dengan bentuk tubuhnya yang luar biasa proporsional dan simetri, serta kekuatan fisiknya yang mengesankan.</p>
