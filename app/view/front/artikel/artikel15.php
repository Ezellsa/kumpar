    <h1>Tips dan Cara untuk memulai diet cutting anda</h1>
<img src="<?=base_url()?>media/istockphoto-480391926-612x612.jpg">
<h2>1.Hitung Kebutuhan Kalori</h2>
<p> Langkah pertama dalam diet cutting adalah menentukan kebutuhan kalori harian Anda. Gunakan kalkulator online atau konsultasikan dengan ahli gizi untuk menentukan jumlah kalori yang diperlukan untuk menjaga berat badan Anda saat ini. Kemudian, kurangi kalori ini sekitar 500-750 kalori per hari untuk menciptakan defisit kalori yang diperlukan untuk penurunan berat badan.</p>
<br>
<h2>2.Prioritaskan Protein</h2>
<img src="<?=base_url()?>media/Kategori-Protein.jpg.webp">
<p>Protein sangat penting dalam diet cutting untuk menjaga massa otot saat menurunkan lemak tubuh. Pastikan untuk memasukkan sumber protein berkualitas seperti daging tanpa lemak, ikan, telur, dan produk susu rendah lemak ke dalam setiap makanan Anda. Usahakan untuk mengonsumsi sekitar 1,2-1,5 gram protein per kilogram berat badan Anda setiap hari.</p>
<br>
<h2>3.Fokus pada Karbohidrat Kompleks</h2>
<img src="<?=base_url()?>media/20220608092755_original.webp">
<p>Pilih karbohidrat kompleks seperti roti gandum, nasi merah, oatmeal, dan sayuran berdaun hijau untuk memberikan energi yang stabil dan menghindari lonjakan gula darah yang cepat. Batasi konsumsi karbohidrat sederhana seperti roti putih, pasta putih, dan makanan manis yang tinggi gula.</p>
<br>
<h2>4.Perhatikan asupan lemak</h2>
<img src="<?=base_url()?>media/mengenal-lemak-sehat-dan-sumbernya.jpg">
<p>Lemak sehat seperti lemak tak jenuh tunggal dan lemak tak jenuh ganda penting untuk kesehatan jantung dan fungsi tubuh lainnya. Sumber lemak sehat termasuk alpukat, kacang-kacangan, biji-bijian, minyak zaitun, dan ikan berlemak seperti salmon.</p>
<br>
<h2>5.Sesuaikan Porsi Makanan</h2>
<img src="<?=base_url()?>media/Porsi-Makan-1.png">
<p>Perhatikan ukuran porsi makanan Anda dan hindari makan berlebihan. Makan dalam porsi yang moderat dan penuhi piring Anda dengan banyak sayuran hijau, protein berkualitas, dan karbohidrat kompleks</p>
<h2>6.Lakukan Latihan Kekuatan</h2>
<img src="<?=base_url()?>media/gym-workout-plans-for-cutting.jpg">
<p>Latihan kekuatan atau resistensi penting untuk mempertahankan massa otot selama diet cutting. Latihan beban membantu membakar kalori lebih banyak, meningkatkan metabolisme, dan memperkuat otot Anda.</p>
<br>
<h2>7. Pantau Progress Anda</h2>
<img src="<?=base_url()?>media/download (2).jpeg">
<p>Tetapkan tujuan yang realistis dan pantau kemajuan Anda secara teratur. Gunakan alat seperti timbangan, pengukur lingkar tubuh, dan foto sebelum dan sesudah untuk melacak perubahan dalam komposisi tubuh Anda.</p>
<br>
<p>Dengan mengikuti tips dan cara ini, Anda dapat melakukan diet cutting secara efektif dan mencapai tujuan penurunan lemak tubuh Anda dengan sukses. Pastikan untuk konsultasikan dengan ahli gizi atau pelatih pribadi jika Anda memiliki pertanyaan atau kekhawatiran tentang program diet cutting Anda.</p>








