<h1>Asal Usul Game League of Legend</h1>
<br>
<img src="<?=base_url()?>media/league-of-legends-champions-i102252.jpg">
<p text align="justify">League of Legends (LoL) adalah salah satu permainan multiplayer online battle arena (MOBA) yang paling terkenal dan berpengaruh dalam industri permainan video. Asal usul permainan ini dimulai pada awal tahun 2000-an, ketika dua pemrogram permainan video, Marc "Tryndamere" Merrill dan Brandon "Ryze" Beck, bertemu di Universitas California, Los Angeles (UCLA). Keduanya memiliki hasrat yang sama terhadap permainan video dan memiliki ide untuk membuat permainan yang revolusioner di industri tersebut.

    Pada tahun 2006, Merrill dan Beck mendirikan perusahaan yang mereka beri nama "Riot Games" dengan tujuan untuk menciptakan permainan yang bisa mengubah cara orang bermain dan berinteraksi dalam permainan video. Setelah berbagai iterasi dan pengujian, League of Legends dirilis secara resmi pada tahun 2009.
    
    Dalam League of Legends, pemain memainkan peran sebagai "summoner" yang memimpin timnya dalam pertempuran melawan tim lawan. Setiap pemain memilih karakter yang disebut "champion" yang memiliki kemampuan dan keahlian unik. Tujuan utama dari permainan ini adalah untuk menghancurkan "Nexus" lawan, struktur sentral yang berada di pangkalan mereka, sambil melindungi Nexus tim sendiri dari serangan lawan.</p>
<br>
<img src="<?=base_url()?>media/League_of_Legends.webp">
<p text align="justify">Sejak diluncurkan, League of Legends telah berkembang pesat dan menjadi salah satu permainan video paling populer di dunia. Turnamen dan kompetisi LoL, seperti Kejuaraan Dunia League of Legends (Worlds), telah menarik jutaan penonton dari seluruh dunia dan menawarkan hadiah besar bagi para pemain terbaik. LoL juga memiliki komunitas yang besar dan bersemangat, dengan forum diskusi, acara live streaming, dan konten kreator yang aktif.
<p text align="justify">
    League of Legends tidak hanya dikenal karena gameplay yang menarik dan kompetitifnya, tetapi juga karena cerita dan desain dunia fiksinya yang mendalam. Para pemain terlibat dalam konflik antar fraksi dan karakter yang kaya, yang semakin diperluas melalui cerita, komik, dan acara di luar permainan.
    <p text align="justify">
    Secara keseluruhan, League of Legends telah menjadi salah satu ikon dalam industri permainan video, menetapkan standar untuk permainan MOBA dan mempengaruhi budaya gaming secara luas.</p>
