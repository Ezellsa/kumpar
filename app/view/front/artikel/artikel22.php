<p text align="justify">Dua hari kemudian, setelah kabar menyebar dari provinsi selatan, the Order of Shadow tiba di tempat mengerikan itu. Pemimpin mereka, Zed, mengetahui area ini tak memiliki manfaat dari segi taktik. Pembantaian ini ditujukan sebagai sebuah pesan. Noxus tak akan menunjukkan pengampunan.
    <p text align="justify">
    Sebuah pantulan cahaya dari baja menarik perhatiannya. Seorang anak yang berumur tak lebih dari sepuluh tahun terbaring di lumpur, mengangkat sabit rusaknya kea rah sang master assassin, tangannya dipenuhi darah. Mata anak laki-laki ini menyimpan rasa sakit yang melebihi umurnya, tapi dia masih terbakar amarah seorang pejuang terlatih. Keteguhan seperti itu tak bisa diajari. Zed melihat di diri anak ini, orang Noxus yang berhasil selamat ini telah ditinggalkan, dia dapat menjadi senjata yang bisa dikerahkan pada mereka yang mengirimnya untuk mati. Sang assassin mengulurkan tangannya dan menyambut Kayn ke dalam Order of Shadow.
    <p text align="justify">
    Pendeta biasanya menghabiskan latihan bertahun-tahun untuk menguasai satu senjata pilihannya, tapi Kayn menguasai semuanya—baginya, mereka hanyalah alat, dan dialah senjatanya. Armor dia anggap hanyalah sebuah beban, maka dia menutupi dirinya dengan bayangan dan membunuh musuhnya dengan kecepatan dan kegesitan. Eksekusi singkat itu menanamkan rasa takut di hati mereka yang beruntung dibiarkan hidup.
    <p text align="justify">
    Saat legenda Kayn menyebar, begitu pula arogansinya. Dia meyakini kekuatannya suatu hari nanti dapat melampaui Zed.
    <p text align="justify">
    Kesombongan itu membawa Kayn menuju ujian terkahirnya: untuk mencari senjata darkin yang baru saja ditemukan di Noxus, dan mencegahnya untuk digunakan melawan pelindung Ionia. Dia menerimanya tanpa ragu, tanpa bertanya kenapa dia dipilih untuk menjalankan tugas ini. Tentu saja, pendeta lain akan menghancurkan sabit hidup yang dikenal bernama Rhaast, tapi Kayn mengambilnya untuk digunakan sendiri.
    <p text align="justify">
    Perusakan mulai menguasainya ketika jari-jarinya menggenggam senjata itu, mengunci mereka dalam perjuangan yang menentukan. Rhaast telah lama menunggu inang yang sempurna untuk kembali bergabung kembali dengan sesame kaum darkin dan melenyapkan dunia, tapi Kayn tidak mudah dikuasai. Dia kembali ke Ionia dengan kemenangan, dia meyakinkan Zed untuk menjadikannya pemimpin baru Order of Shadow.</p>

















