<p>Creatine adalah senyawa organik yang secara alami terdapat dalam tubuh manusia, terutama di otot skelet. Senyawa ini memiliki peran penting dalam menyediakan energi bagi sel-sel otot selama aktivitas fisik yang intens. Dalam bentuk suplemen, creatine telah menjadi salah satu suplemen ergogenik yang paling banyak digunakan dan dipelajari dalam dunia olahraga dan kebugaran.
<p>
    Creatine umumnya dihasilkan dalam tubuh dari tiga asam amino: arginin, glikin, dan metionin. Sebagian besar creatine disimpan dalam otot dalam bentuk fosfokreatin. Ketika diperlukan, fosfokreatin memecah dirinya untuk menghasilkan ATP (adenosin trifosfat), sumber energi utama bagi sel-sel tubuh. Ini membuat creatine menjadi kunci dalam pemulihan energi cepat selama latihan anaerobik berat, seperti angkat beban atau sprint.</p>
<br>
<img src="<?=base_url()?>media/Creatine.webp">
<p>Penggunaan suplemen creatine telah terbukti meningkatkan kinerja atletik dan peningkatan massa otot, terutama pada latihan resistensi yang intens. Dalam beberapa penelitian, creatine juga telah terbukti dapat meningkatkan kekuatan, kecepatan, dan daya tahan otot. Meskipun efeknya bervariasi antar individu, banyak penelitian menunjukkan bahwa creatine memberikan manfaat yang signifikan bagi para atlet yang terlibat dalam latihan anaerobik.
<p>
    Suplemen creatine biasanya dikonsumsi dalam bentuk monohidrat creatine, yang larut dalam air. Pada tahap awal penggunaan, biasanya dilakukan periode pengisian (loading phase) dengan dosis yang lebih tinggi, diikuti oleh periode pemeliharaan dengan dosis yang lebih rendah. Dosis standar adalah sekitar 3-5 gram per hari untuk pemeliharaan. Namun, perlu dicatat bahwa setiap orang memiliki toleransi yang berbeda terhadap creatine, dan konsultasi dengan profesional medis sebelum mengambil suplemen sangat dianjurkan.</p>
<br>
